use std::fs;
use std::path::{Path, PathBuf};
use std::process::Command;
use std::sync::mpsc;
use std::time::Duration;

use clap::{Parser, Subcommand, ValueEnum};
use clawc::codegen;
use clawc::config::{BuildLanguage, ClawConfig};
use clawc::errors::CompilerError;
use clawc::{parser, semantic};
use notify::{recommended_watcher, RecursiveMode, Watcher};
use thiserror::Error;

#[derive(Debug, Parser)]
#[command(author, version, about = "Claw language CLI")]
struct Cli {
    #[command(subcommand)]
    command: Commands,
}

#[derive(Debug, Subcommand)]
enum Commands {
    Init(InitArgs),
    Build(BuildArgs),
    Dev(DevArgs),
    Test(TestArgs),
    Run(RunArgs),
    Chat(ChatArgs),
}

#[derive(Debug, clap::Args)]
struct RunArgs {
    /// Workflow name to execute
    workflow: String,
    /// Arguments in key=value format
    #[arg(long = "arg", value_name = "KEY=VALUE")]
    args: Vec<String>,
    /// Path to runtime.js (defaults to generated/runtime.js)
    #[arg(long)]
    runtime: Option<PathBuf>,
}

#[derive(Debug, clap::Args)]
struct ChatArgs {
    /// Path to runtime.js (defaults to generated/runtime.js)
    #[arg(long)]
    runtime: Option<PathBuf>,
}

#[derive(Debug, clap::Args)]
struct InitArgs {
    #[arg(long, default_value = "claw.json")]
    path: PathBuf,
    #[arg(long)]
    force: bool,
}

#[derive(Debug, Clone, Copy, ValueEnum, PartialEq, Eq)]
enum Language {
    Opencode,
    Ts,
    Python,
}

impl From<Language> for BuildLanguage {
    fn from(lang: Language) -> Self {
        match lang {
            Language::Opencode => BuildLanguage::Opencode,
            Language::Ts => BuildLanguage::Ts,
            Language::Python => BuildLanguage::Python,
        }
    }
}

#[derive(Debug, clap::Args)]
struct BuildArgs {
    source: Option<PathBuf>,
    #[arg(long, value_enum)]
    lang: Option<Language>,
    #[arg(long)]
    watch: bool,
    #[arg(long, default_value = "claw.json")]
    config: PathBuf,
}

#[derive(Debug, clap::Args)]
struct DevArgs {
    #[arg(long, default_value = "claw.json")]
    config: PathBuf,
}

#[derive(Debug, clap::Args)]
struct TestArgs {
    source: Option<PathBuf>,
    #[arg(long)]
    filter: Option<String>,
    #[arg(long, default_value = "claw.json")]
    config: PathBuf,
}

#[derive(Debug, Error)]
enum ClawCliError {
    #[error("{rendered}")]
    Compiler {
        error: CompilerError,
        rendered: String,
    },
    #[error("{0}")]
    Message(String),
}

fn main() {
    if let Err(error) = run(Cli::parse()) {
        eprintln!("{error}");
        std::process::exit(exit_code_for_error(&error));
    }
}

fn exit_code_for_error(error: &ClawCliError) -> i32 {
    match error {
        ClawCliError::Compiler { error, .. } => match error {
            CompilerError::ParseError { .. } => 1,
            CompilerError::UndefinedTool { .. }
            | CompilerError::UndefinedAgent { .. }
            | CompilerError::UndefinedClient { .. }
            | CompilerError::UndefinedType { .. }
            | CompilerError::TypeMismatch { .. }
            | CompilerError::DuplicateDeclaration { .. }
            | CompilerError::CyclicDependency { .. }
            | CompilerError::MissingReturn { .. }
            | CompilerError::InvalidControlFlow { .. }
            | CompilerError::InvalidAssertOutsideTest { .. }
            | CompilerError::CircularType { .. } => 2,
            CompilerError::CodegenError { .. }
            | CompilerError::BamlSignatureConflict { .. } => 3,
            CompilerError::IoError { .. } => 4,
            _ => 1,
        },
        ClawCliError::Message(_) => 1,
    }
}

fn run(cli: Cli) -> Result<(), ClawCliError> {
    match cli.command {
        Commands::Init(args) => run_init(args),
        Commands::Build(args) => run_build(args),
        Commands::Dev(args) => run_dev(args),
        Commands::Test(args) => run_test(args),
        Commands::Run(args) => run_run(args),
        Commands::Chat(args) => run_chat(args),
    }
}

