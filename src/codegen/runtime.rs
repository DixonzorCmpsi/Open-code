// src/codegen/runtime.rs
// Generates generated/runtime.js — a self-contained headless workflow runner.
// Uses zero npm dependencies (raw fetch for all LLM providers).
// See specs/38-Closed-Loop-Runtime.md and specs/39-Runtime-First-Architecture.md

use std::fs;
use std::path::Path;

use crate::ast::*;
use crate::errors::{CompilerError, CompilerResult};

use super::shared_js;

pub fn generate(document: &Document, project_root: &Path) -> CompilerResult<()> {
    let out_dir = project_root.join("generated");
    fs::create_dir_all(&out_dir)
        .map_err(|e| CompilerError::IoError { message: format!("failed to create generated/: {e}"), span: 0..0 })?;

    let js = emit_runtime(document);

    fs::write(out_dir.join("runtime.js"), js)
        .map_err(|e| CompilerError::IoError { message: format!("failed to write runtime.js: {e}"), span: 0..0 })?;

    Ok(())
}

fn emit_runtime(document: &Document) -> String {
    let mut out = String::new();
    out.push_str(RUNTIME_HEADER);
    out.push_str(&emit_schemas(document));
    out.push_str(&emit_tool_descriptors(document));
    out.push_str(&emit_tool_handlers(document));
    out.push_str(&emit_agent_runners(document));
    out.push_str(&emit_agents(document));
    out.push_str(&emit_plans(document));
    out.push_str(WORKFLOW_EXECUTOR);
    out.push_str(CLI_ENTRY);
    out
}

