// src/codegen/python.rs
// Generates generated/claw/__init__.py — a self-contained BAML-style Python client.
// No claw_sdk dependency. Only pip deps: pydantic, httpx.
// Each workflow compiles to a typed importable async function.
// Supports artifact placement: workflow { artifact { format = "..." path = "..." } }

use std::collections::HashSet;

use crate::ast::{
    AgentDecl, Block, Constraint, DataType, Document, Expr, SettingValue, SpannedExpr, Statement,
    ToolDecl, TypeDecl, TypeField,
};
use crate::errors::{CompilerError, CompilerResult};

pub(super) fn generate(document: &Document) -> CompilerResult<String> {
    let mut out = String::new();

    // 1. File header + imports
    out.push_str(HEADER);
    out.push_str("\n\n");

    // 2. Pydantic models
    for t in &document.types {
        out.push_str(&render_model(t)?);
        out.push_str("\n\n");
    }

    // 3. Tool handlers
    out.push_str(&emit_tool_handlers(document));

    // 4. Agent runners
    out.push_str(&emit_agent_runners(document));

    // 5. Workflow functions
    for wf in &document.workflows {
        out.push_str(&render_workflow(wf, document)?);
        out.push_str("\n\n");
    }

    // 6. CLI entry
    out.push_str(&emit_cli_entry(document));

    Ok(out)
}

